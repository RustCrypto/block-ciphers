#[inline(always)]
pub(crate) unsafe fn parallelize_keys(keys: &RoundKeys<13>) -> Simd256RoundKeys<13> {
    let mut v256: [MaybeUninit<__m256i>; 13] = MaybeUninit::uninit().assume_init();
    asm! {
        "vbroadcastf128 ymm0 , [{keys} +  0 * 16]",
        "vbroadcastf128 ymm1 , [{keys} +  1 * 16]",
        "vbroadcastf128 ymm2 , [{keys} +  2 * 16]",
        "vbroadcastf128 ymm3 , [{keys} +  3 * 16]",
        "vbroadcastf128 ymm4 , [{keys} +  4 * 16]",
        "vbroadcastf128 ymm5 , [{keys} +  5 * 16]",
        "vbroadcastf128 ymm6 , [{keys} +  6 * 16]",
        "vbroadcastf128 ymm7 , [{keys} +  7 * 16]",
        "vbroadcastf128 ymm8 , [{keys} +  8 * 16]",
        "vbroadcastf128 ymm9 , [{keys} +  9 * 16]",
        "vbroadcastf128 ymm10, [{keys} + 10 * 16]",
        "vbroadcastf128 ymm11, [{keys} + 11 * 16]",
        "vbroadcastf128 ymm12, [{keys} + 12 * 16]",

        "vmovdqu [{optr} +  0 * 32], ymm0 ",
        "vmovdqu [{optr} +  1 * 32], ymm1 ",
        "vmovdqu [{optr} +  2 * 32], ymm2 ",
        "vmovdqu [{optr} +  3 * 32], ymm3 ",
        "vmovdqu [{optr} +  4 * 32], ymm4 ",
        "vmovdqu [{optr} +  5 * 32], ymm5 ",
        "vmovdqu [{optr} +  6 * 32], ymm6 ",
        "vmovdqu [{optr} +  7 * 32], ymm7 ",
        "vmovdqu [{optr} +  8 * 32], ymm8 ",
        "vmovdqu [{optr} +  9 * 32], ymm9 ",
        "vmovdqu [{optr} + 10 * 32], ymm10",
        "vmovdqu [{optr} + 11 * 32], ymm11",
        "vmovdqu [{optr} + 12 * 32], ymm12",

        keys = in(reg) keys.as_ptr(),
        optr = in(reg) v256.as_mut_ptr().cast::<__m256i>(),

        clobber_abi("C"),
        options(nostack, preserves_flags),
    };
    core::mem::transmute(v256)
}

